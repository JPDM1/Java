//Quiero expresar una variable de tipo entero
public class Variables {
    public static void main(String[] args) {
        byte numero = 5;
        float numero2 = 5.5f; //poner siempre la f para que indique que es un float
        char curso='A'; // solo entiende la comilla simple
        boolean bandera = true; // true o false
        System.out.println(numero2);   
        System.out.println(numero);   
        System.out.println(bandera);   
        System.out.println(curso);   
    }
}